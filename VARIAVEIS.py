# Codigo que pergunte dois numeros 
# Faca a soma, substracao, multiplicacao e divisao

primeiro = int(input("Digite o primeiro numero: ")) # centro de armazenagem do primeiro comando
print(primeiro)

segundo = int(input("Digite o segundo numero: ")) # centro de armazenagem do segundo comando
print(segundo)

# Realizando as operacoes anteriores

soma = primeiro + segundo
print(soma)

subtracao = primeiro - segundo
print(subtracao)

multiplicacao = primeiro * segundo
print(multiplicacao)

divisao = primeiro / segundo
print(divisao)



